function jumlahVolumeDuakubus (a, b){
    let total, volumeA, volumeB;
    volumeA = a * a * a
    volumeB = b * b * b
    total = volumeA + volumeB
    return total
}
console.log(jumlahVolumeDuakubus(3, 4))
console.log(jumlahVolumeDuakubus(8, 30))

